def get_lay_term_glossary_prompt(retrieved_chunks:str):
    combined_text = retrieved_chunks.lower()
    mini_glossary = {}
    
    # iterate through each category and term in glossary
    for category, terms_list in lay_term_glossary_dictionery.items():
        for term_entry in terms_list:
            scientific_term = term_entry["Scientific Term"]
            
            # check if the scientific term appears in the retrieved text
            if scientific_term.lower() in combined_text:
                # add the category if it doesnt exist
                if category not in mini_glossary:
                    mini_glossary[category] = []

                mini_glossary[category].append(term_entry)
    
    # format the glossary as text for system prompt
    result = []
    for category, terms in mini_glossary.items():
        result.append(f"### {category}")
        
        for term in terms:
            scientific_term = term["Scientific Term"]
            lay_term = term["Lay Term"]
            term_type = term["Short Term"]
            
            if term_type == "Short Term":
                result.append(f"- {scientific_term}: use '{lay_term}'")
            else:
                result.append(f"- {scientific_term}: {lay_term}")
        
        result.append("")  # Empty line between categories
    
    # if no glossary terms are found, return a blank prompt for glossary
    if not result:
        return ''
    
    mini_glossary_text = "\n".join(result)

    base_prompt = '''When responding:
1. Identify scientific/medical terms in your answer.
2. Replace each scientific term with its lay equivalent, placing the scientific term in brackets afterward.
   Example: "chest pain (angina pectoris)" instead of just "angina pectoris"

3. When multiple lay term options exist:
   - Use the Short Term version for initial mentions
   - Use the Definition version when more explanation is needed
   - Both can be used if appropriate

4. For any scientific terms found in the content that are not in the provided mini-glossary, use your judgment to provide an appropriate lay explanation.

Below is a glossary of medical terms relevant to this query. Always use these specific lay terms when the scientific terms appear:
'''

    glossary_prompt = base_prompt + mini_glossary_text

    return glossary_prompt